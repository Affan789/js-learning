// truthy and falsy values

// falsy value:
// false
// ""
// null
// undefined
// 0

// truthy values:
// a,b,c
// 1,-1

// these values show that these are the true and false conditions for if-else
// for example:
// for falsy:

let Name = 0;
if(Name){
    console.log(Name);
}else{
    console.log("empty answer");
}
// for truthy:

let key = -1;
if(key){
    console.log(key);

}else{
    console.log("empty answer");
}