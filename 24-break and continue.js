// Break and Continue
// Break:
// the Break keyword is used if we want to stop the condition and move out of it
// for example:

 for(let i = 0;i<=10;i++){
     if(i===4){
         break;
     }
     console.log(i);
 }
// Continue:
// the continue keyword sends the execution right back to the start
// for example:

 for(let g = 0;g<=10;g++){
     if(g===4){
         continue;
     }
       console.log(g);
    }   
 
// the difference between break and continue is that break stopped the whole condition and continue only skipped a part of the condition and continued the execution
