// and or operator
let Name = "affan";
let age = 18;
if(Name[0]==="a" && age>=18){
    console.log("your name is affan and your age is 18");
}else{
    console.log("wrong person");
}
// in AND operator if any one condition is false then the whole condition is false

// OR operator
let FirstName = "muhammad";
let size = 18;
if(FirstName[0]==="g"|| size>=21){
    console.log("your condition is true");
}else{
    console.log("your condition is false");
}
// in OR operator if even any one condition is true then the whole condition is true.this operator is the opposite of the AND operator
