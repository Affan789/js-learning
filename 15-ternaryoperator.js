// ternary operator /conditional operator
let age = 12;
let drink;
if(age>=10){
    drink = "coffee";

}else{
    drink = "milk";
}
console.log(drink);
// as you can see this process is a bit long so now we are going to use the ternary operator method

let size = 4;
let liquid = size>=10 ? "coffee": "milk";
console.log(liquid);
