// dot vs bracket notation
// bracket notation


const Human = {Name:"affan",Age:"18",School:"fox",Hobbies:["playing games","reading books"]};
console.log(Human["Hobbies"]);
 Human["email"]="asasgamer112@gmail.com";
console.log(Human);

// dot notation:
console.log(Human.Hobbies);
Human.email="asasgamer112@gmail.com";
console.log(Human.email);
console.log(Human);



