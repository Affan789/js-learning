// string indexing
// if we want to print a specific character of a value it is done by using string indexing
let firstName = "affan";
// a f f a n
// 0 1 2 3 4
console.log(firstName[2]);
// and if we want to find the length of the index 
console.log(firstName.length);
// and if we want to find the last character of the index
console.log(firstName[firstName.length-1]);
